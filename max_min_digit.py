# Задача: Побудова мінімального та максимального числа з цифр
#
# Нехай A = [a₁, a₂, a₃, ..., aₙ] — масив з десяткових цифр.
# Напишіть програму, яка складе з заданих цифр мінімальне число та максимальне число.
# Треба використати всі цифри, перша цифра числа не може бути нулем.

n = int(input())  # Зчитуємо кількість цифр
A = list(map(int, input().split()))  # Зчитуємо сам масив цифр

# Створюємо найбільше можливе число — просто сортуємо за спаданням
max_digits = sorted(A, reverse=True)
max_number = ''.join(map(str, max_digits))  # Об'єднуємо цифри в рядок

# Створюємо найменше можливе число
min_digits = sorted(A)  # Сортуємо цифри за зростанням

# Перевіряємо, чи перша цифра — нуль (що недопустимо для числа)
if min_digits[0] == 0:
    # Знаходимо першу ненульову цифру і міняємо її місцями з нулем
    for i in range(1, n):
        if min_digits[i] != 0:
            min_digits[0], min_digits[i] = min_digits[i], min_digits[0]
            break

min_number = ''.join(map(str, min_digits))  # Об'єднуємо у фінальний рядок

# Виводимо найменше та найбільше число
print(min_number)
print(max_number)
