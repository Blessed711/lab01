# Задача: Сортування парних і непарних елементів масиву
#
# Нехай A = [a₁, a₂, a₃, ..., aₙ] — масив цілих чисел.
# Напишіть програму, яка відсортує за неспаданням парні елементи масиву,
# а непарні елементи також відсортує за неспаданням.

n = int(input())  # Зчитуємо кількість елементів у масиві
A = list(map(int, input().split()))  # Зчитуємо масив цілих чисел

# Сортуємо всі парні числа за зростанням
even_sorted = sorted(x for x in A if x % 2 == 0)

# Сортуємо всі непарні числа за зростанням
odd_sorted = sorted(x for x in A if x % 2 != 0)

result = []    # Результуючий масив
i_even = 0     # Індекс у відсортованому списку парних
i_odd = 0      # Індекс у відсортованому списку непарних

# Проходимо по всьому вхідному масиву і вставляємо відповідні (вже відсортовані) елементи
for x in A:
    if x % 2 == 0:
        result.append(even_sorted[i_even])  # Вставляємо чергове парне
        i_even += 1
    else:
        result.append(odd_sorted[i_odd])    # Вставляємо чергове непарне
        i_odd += 1

# Виводимо результат через пробіл
print(*result)
